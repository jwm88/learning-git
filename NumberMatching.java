public class NumberMatching {
public static void main(String[] args) {
    int num = 123;
    switch (num) {

        case 1 :
        System.out.println("I've got 1");
        break;

        case 2 :
        System.out.println("I've got 2");
        break;

        case 3:
        System.out.println("I've got 3");
        break;

        default:
        System.out.println("I've got "+ num);
        break;
    }
}   
}
