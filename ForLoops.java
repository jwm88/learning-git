public class ForLoops {

    public static void main(String[] args) {

        //write a program that counts the numbers from 1 to 20
        //it prints the number if it's a multiple of 3

        for (int i=1; i<=20; i++) {
            if (i%3==0) {
                System.out.println(i);
            }
        }
    
}
}
